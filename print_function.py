# Task 1: Basic Printing

# Print the phrase 'Hello, World!'
print("Hello, World!")

# Print your first name
print("Adnan")

# Task 2: Printing Multiple Items

# Print your first name and last name in a single print statement
print("Adnan Sabir")

# Print the numbers 1, 2, and 3 in a single print statement
print(1, 2, 3)

# Task 3: Printing Special Characters

# Print a string that includes a newline character to display text on two lines
print("Hello\nWorld")

# Print a string that includes a tab character
print("Hello\tWorld")

# Task 4: Using the sep Parameter

# Print three different words separated by commas
print("apple", "banana", "cherry", sep=", ")

# Print three different numbers separated by hyphens
print(1, 2, 3, sep="-")

# Task 5: Using the end Parameter

# Print two words on the same line with a space in between
print("Hello", end=" ")
print("World")

# Print the numbers 1 and 2 on the same line without a space in between
print(1, end="")
print(2)

# Task 6: Escape Characters

# Print a string that includes double quotes
print('He said, "Hello!"')

# Print a backslash character
print("This is a backslash: \\")

# Task 7: Combining Text and Numbers

# Print your age alongside a descriptive message
print("I am", 25, "years old")

# Print a number and a string together
print("The number is", 5)

# Task 8: Basic Loop for Printing

# Print the numbers 1 to 5, each on a new line using separate print statements
print(1)
print(2)
print(3)
print(4)
print(5)